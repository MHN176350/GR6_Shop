package Model;

/**
 *
 * @author Minh
 */
public class PieChart {
   public String data;
   public double value;

    public PieChart() {
    }

    public PieChart(String data, double value) {
        this.data = data;
        this.value = value;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public double getValue() {
        return value;
    }

    public void setValue(double value) {
        this.value = value;
    }

    @Override
    public String toString() {
        return super.toString(); 
    }
}
