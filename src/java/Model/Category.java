public class Category {

    private int id;
    private String name;
    private int quntity;

    public Category() {
    }

    public Category(int id, String name, int quntity) {
        this.id = id;
        this.name = name;
        this.quntity = quntity;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getQuntity() {
        return quntity;
    }

    public void setQuntity(int quntity) {
        this.quntity = quntity;
    }

   

}
